// CipherType Declaration

//! CipherType an Enum that stores the config about the cipher method that you are using
//! *** for example:
//!         CipherType::SUBSTITUTION {
//!             subst: ['a','b','c'...],  # as the list of SUBSTITUTION
//!             ... # to more think about
//!         }
#[derive(Debug)]
pub(crate) enum CipherType {
    SUBSTITUTION,
    TRANSPOSITION,
    MONOALPHABET,
    POLYALPHABET,
    POLYGRAPHIC,
}

// CipherType Implementation

/// Implementation of a default function to assign default config
impl CipherType {
    fn default(self) -> Self {
        match self {
            CipherType::SUBSTITUTION => Self::SUBSTITUTION,
            CipherType::TRANSPOSITION => Self::TRANSPOSITION,
            CipherType::MONOALPHABET => Self::MONOALPHABET,
            CipherType::POLYALPHABET => Self::POLYALPHABET,
            CipherType::POLYGRAPHIC => CipherType::POLYGRAPHIC,
        }
    }
}

#[derive(Debug, thiserror::Error)]
pub enum CipherError {
    #[error("The given key is not alphabet.")]
    NotAlphabet,
    #[error("The given word to get encrypted have repeated elements")]
    RepeatedInput,
}

pub(crate) type CipherResult = Result<String, CipherError>;

pub trait Cipher {
    fn cipher(&self, plain_text: &str) -> CipherResult;
    fn decipher(&self, cipher_text: &str) -> CipherResult;
}

